const title = "On-chain data availability";

class GlossaryContent extends React.Component {
	render() {
		return (
			<div className={"glossary-content"}>
				<p>
					On-chain data availability occurs when an L2 publishes its transaction data to its designated L1. For example, a Celestia L2 that
					publishes its transaction data to Celestia is using on-chain data availability. If that Celestia L2 publishes its transaction data
					somewhere other than Celestia, it is using off-chain data availability. L2’s that use on-chain data availability are called
					rollups while L2’s that use off-chain data availability are called validiums or plasma.
				</p>
			</div>
		);
	}
}

